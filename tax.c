#include <stdio.h>

#define 기본시급 12000
#define 초과수당분당 100

int main() {
    int 사원번호, 나이, 부양가족수;
    char 부서코드;
    int 근무시간초;
    double 신용카드사용액, 체크카드사용액;

    printf("사원번호: ");
    scanf_s("%d", &사원번호);

    printf("나이: ");
    scanf_s("%d", &나이);

    if (나이 < 18) {
        printf("고용 불가 나이입니다.\n");
        return 0;
    }

    printf("부서코드(A, B, C): ");
    scanf_s(" %c", &부서코드, 1);

    printf("근무시간(초): ");
    scanf_s("%d", &근무시간초);

    printf("부양가족 수: ");
    scanf_s("%d", &부양가족수);

    printf("신용카드 사용액(원): ");
    scanf_s("%lf", &신용카드사용액);

    printf("체크카드 사용액(원): ");
    scanf_s("%lf", &체크카드사용액);

    int 시 = 근무시간초 / 3600;
    int 분 = (근무시간초 % 3600) / 60;
    int 초 = 근무시간초 % 60;

    int 근무시간분 = (근무시간초 + 59) / 60;

    int 최대근무분;
    if (나이 < 25)
        최대근무분 = 7 * 60;
    else if (나이 < 55)
        최대근무분 = 9 * 60;
    else if (나이 < 65)
        최대근무분 = 5 * 60;
    else
        최대근무분 = 3 * 60;

    int 기본근무분 = 근무시간분;
    if (근무시간분 > 최대근무분)
        기본근무분 = 최대근무분;

    int 초과근무분 = 근무시간분 - 기본근무분;

    double 기본급 = (기본근무분 / 60.0) * 기본시급;
    double 초과급 = 초과근무분 * 초과수당분당;
    double 세전급여 = 기본급 + 초과급;

    double 세율;
    if (세전급여 < 30000)
        세율 = 0.05;
    else if (세전급여 < 70000)
        세율 = 0.10;
    else
        세율 = 0.15;

    double 세금 = 세전급여 * 세율;

    double 환급금 = 부양가족수 * 2000;
    if (신용카드사용액 > 세전급여 * 0.3)
        환급금 += (신용카드사용액 - 세전급여 * 0.3) * 0.1;
    if (체크카드사용액 > 세전급여 * 0.3)
        환급금 += (체크카드사용액 - 세전급여 * 0.3) * 0.2;

    double 세후급여 = 세전급여 - 세금 + 환급금;

    char 부서명[20];

    if (부서코드 == 'A')
        sprintf_s(부서명, sizeof(부서명), "기획실");
    else if (부서코드 == 'B')
        sprintf_s(부서명, sizeof(부서명), "총무과");
    else if (부서코드 == 'C')
        sprintf_s(부서명, sizeof(부서명), "영업부");
    else
        sprintf_s(부서명, sizeof(부서명), "기타부서");

    printf("\n%d번 사원 급여 정보입니다.\n", 사원번호);
    printf("%d시간 %d분 %d초 근무자, ", 시, 분, 초);
    if (초과근무분 > 0)
        printf("%d시간 %d분 초과근무, ", 초과근무분 / 60, 초과근무분 % 60);
    else
        printf("초과근무 없음, ");
    printf("%s 직원입니다.\n", 부서명);

    printf("세전급여: %.0f원, 세금: %.0f원, 환급금: %.0f원\n", 세전급여, 세금, 환급금);
    printf("세후급여: %.0f원\n", 세후급여);

    return 0;
}
